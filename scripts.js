    function displayAnimeByGenre(genre) {
        animeContainer.innerHTML = ''; // Clear previous content

        if (animeData[genre]) {
            animeData[genre].forEach(anime => {
                const animeCard = document.createElement('div');
                animeCard.classList.add('anime-card');
                animeCard.innerHTML = `
                <img src="${anime.image}" alt="${anime.title}" class="anime-image">
                    <h3>${anime.title}</h3>
                    <p>${anime.description}</p>
                `;
                
                animeCard.addEventListener('click', () => {
                    // Save anime data to localStorage
                    localStorage.setItem('selectedAnime', JSON.stringify(anime));
                    // Redirect to the detail page
                    window.location.href = 'anime-detail.html';
                });

                animeContainer.appendChild(animeCard);
            });
        }
    }
